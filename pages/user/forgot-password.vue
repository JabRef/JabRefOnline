<template>
  <NuxtLayout name="bare">
    <template #side>
      <img
        class="w-11/12 mx-auto"
        src="~/assets/undraw_join_of2w.svg"
      />
    </template>
    <div>
      <h2 class="mb-7 text-center text-5xl font-extrabold text-gray-900">
        Reset Password
      </h2>
      <p
        v-if="called"
        class="text-base"
      >
        An email with instructions on how to reset your password has been sent
        to {{ email }}.
      </p>
      <form
        v-else
        @submit.prevent="forgotPassword()"
      >
        <div class="space-y-5">
          <t-input-group
            label="Email address"
            variant="important"
          >
            <t-input
              v-model="email"
              v-focus
            />
          </t-input-group>
          <div class="py-2 text-center">
            <n-button
              class="w-full"
              type="primary"
              attr-type="submit"
              >Submit</n-button
            >
          </div>
        </div>
      </form>
    </div>
  </NuxtLayout>
</template>
<script lang="ts">
import { useMutation } from '@vue/apollo-composable'
import { gql } from '~/apollo'
definePageMeta({ layout: false })
export default defineComponent({
  name: 'ForgotPassword',
  setup() {
    const email = ref('')
    const {
      mutate: forgotPassword,
      called,
      error,
    } = useMutation(
      gql(/* GraphQL */ `
        mutation ForgotPassword($input: ForgotPasswordInput!) {
          forgotPassword(input: $input) {
            result
          }
        }
      `),
      () => ({
        variables: {
          input: {
            email: email.value,
          },
        },
      })
    )
    return { email, error, called, forgotPassword }
  },
})
</script>
